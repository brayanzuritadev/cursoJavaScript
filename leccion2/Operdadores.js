let a = 2
let b = 7

let z = a + b
console.log("resultado de la suma " + z)

z = a - b
console.log("resultado es " + z)

z = a * b
console.log("resultado es " + z)

z = a / b
console.log("resultado es " + z)

z = a % b //residuo d ela division
console.log("resultado es " + z)

z = a ** b //operador exponencial
console.log("resultado es " + z)

//incremento
//preincremento(el operador ++ antes de la variable)
z = ++a
console.log(z)
console.log(a)
///post incremento (el operador ++ despues de la variable)
z = a++
